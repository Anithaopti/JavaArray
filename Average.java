
public class Average {
	 public static void main(String arg[])	
	 {
	   int n=9,result=0;
	 
	   int a[]= {67,89,5689,67,54,456,45,78,967};
	 
	   
	   for(int i=0;i<n;i++)
	    
		   result=result+a[i];
	 	 	    
	   System.out.println("average of a[] is ="+result/n);
	 
	 }
	
}
